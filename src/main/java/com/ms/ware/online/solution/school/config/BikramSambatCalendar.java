    public int getDaysInMonth(int bsYear, int bsMonth) {
        if (bsMonth < 1 || bsMonth > 12) {
            throw new IllegalArgumentException("Month must be between 1 and 12.");
        }
        int index = bsYear - START_YEAR;
        if (index < 0 || index >= yearMonthDays.size()) {
            throw new IllegalArgumentException("Year out of supported range: " + bsYear);
        }
        return yearMonthDays.get(index)[bsMonth - 1];
    }

    public boolean isSupportedYear(int bsYear) {
        int index = bsYear - START_YEAR;
        return index >= 0 && index < yearMonthDays.size();
    }
